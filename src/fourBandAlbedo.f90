  !! @{
  !! Computes snow grain growth due to deposition
  !! @author M. Namazi &  K. V. Salzen
  !!
  subroutine RDSDPS (SNOW, ZSNOW, SPCP, RHOSNO, REFF, GCROW, &
                     REFF0, DELT, ZSNMIN, ZSNMAX, ILG, IL1, IL2, GCMIN, &
                     GCMAX)
    !
    !     * M. Namazi &  K. V. SALZEN, SNOW GRAIN DUE TO DEPOSITION.
    !
    implicit none

    real, intent(in) :: REFF0
    integer, intent(in) :: ILG, IL1, IL2
    integer :: I
    !
    !     * INPUT/OUTPUT ARRAYS.
    !
    real, DIMENSION(ILG) :: TAUD
    real, DIMENSION(ILG), intent(inout) :: REFF
    real, DIMENSION(ILG), intent(in) :: SNOW, ZSNOW, SPCP, RHOSNO, GCROW
    real, PARAMETER :: SPCMIN = 1.157407407E-8, &
                       TAUINF = 1.E+20
    real, intent(in) :: DELT, ZSNMIN, ZSNMAX, GCMIN, GCMAX
    !---------------------------------------------
    do I = IL1,IL2
      if (GCROW(I) > GCMIN .and. GCROW(I) < GCMAX) then
        if (SNOW(I) > 0.) then
          REFF(I) = MAX(REFF0,REFF(I))
          if (SPCP(I) > SPCMIN) then
            if (ZSNOW(I) < ZSNMAX .and. ZSNOW(I) > ZSNMIN) then
              TAUD(I) = (ZSNOW(I) * RHOSNO(I))/SPCP(I)
              REFF(I) = REFF0 + (REFF(I) - REFF0) * EXP( - DELT/TAUD(I))
            else if (ZSNOW(I) > ZSNMAX) then
              TAUD(I) = (ZSNMAX * RHOSNO(I))/SPCP(I)
              REFF(I) = REFF0 + (REFF(I) - REFF0) * EXP( - DELT/TAUD(I))
            else
              TAUD(I) = 0.
              REFF(I) = 0.
            end if
          else
            TAUD(I) = TAUINF
          end if
        else
          TAUD(I) = 0.
          REFF(I) = 0.
        end if
      end if
    end do ! loop 100

    return
  end subroutine RDSDPS
