  !! @{
  !! Computes snow grain growth due to wet aging
  !! @author M. Namazi, K. V. Salzen
  !!
  subroutine RDSWET (SNOW, WSNOW, REFF, GCROW, REFF0, DELT, ILG, IL1, IL2)
    !
    !     * M. Namazi & K. VON SALZEN, SNOW GRAIN DUE TO WET AGING.
    !     * ONLY OVER LAND HAPPENS.
    !     * FOR THE REST OF REGIONS KEEP THE SAME VALUE,
    !
    implicit none

    !     * INTEGER AND REAL CONSTANTS.
    integer :: I
    integer, intent(in) :: ILG, IL1, IL2
    real, intent(in) :: REFF0
    real, PARAMETER :: PI = 3.1415927, C1 = 4.22E-13

    !     * INPUT ARRAYS.
    real, DIMENSION(ILG), intent(in) :: SNOW, WSNOW, GCROW
    real, intent(in)  :: DELT

    !     * INPUT/OUTPUT ARRAYS.
    real, DIMENSION(ILG), intent(inout) :: REFF

    !     * INTERNAL WORK ARRAYS.
    real, DIMENSION(ILG) :: FLIQ
    !-----------------------------------
    do I = IL1,IL2
      if (GCROW(I) < - 0.5 .and. SNOW(I) > 0.) then
        !           IF (REFF(I)==0)  REFF(I)=REFF0
        REFF(I) = MAX(REFF0,REFF(I))
        REFF(I) = REFF(I) * 1.E+6
        FLIQ(I) = WSNOW(I)/(SNOW(I) + WSNOW(I))
        REFF(I) = ((0.75E+18 * C1/PI) * (FLIQ(I) ** 3) * DELT + REFF(I) ** 3) ** (1./3.)
        REFF(I) = REFF(I) * 1.E-6
      end if
    end do ! loop 100

    return
  end
